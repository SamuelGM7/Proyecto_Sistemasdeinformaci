    private void bbuscaralumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bbuscaralumnoActionPerformed
        // TODO add your handling code here:
        String idStr = tbuscaralumno.getText().trim();
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa un ID para buscar.");
            return;
        }

        try {
            int id = Integer.parseInt(idStr);
            AlumnoDAO dao = new AlumnoDAO();
            Alumno alumno = dao.buscarAlumnoPorId(id);

            if (alumno != null) {
                tnombrealumno.setText(alumno.getNombrealumno());
                tapellidosalumno.setText(alumno.getApellidosalumno());
                tdnialumno.setText(alumno.getDNI());
                tcorreoalumno.setText(alumno.getCorreoalumno());
                combocarrera.setSelectedItem(alumno.getCarrera());
            } else {
                JOptionPane.showMessageDialog(null, "No se encontr√≥ alumno.");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID inv√°lido.");
        }
    }//GEN-LAST:event_bbuscaralumnoActionPerformed

