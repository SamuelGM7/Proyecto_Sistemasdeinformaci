    private void bcrearcarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcrearcarreraActionPerformed
        // TODO add your handling code here:
        String nombre = tcarrerac.getText().trim();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa el nombre de la carrera.");
            return;
        }

        Carrera nuevaCarrera = new Carrera(0, nombre);
        CarreraDAO carreraDAO = new CarreraDAO();

        boolean guardado = carreraDAO.guardarCarrera(nuevaCarrera);

        if (guardado) {
            tcarrerac.setText("");
            cargarCarrerasEnCombo(cbcarrea);
            cargarCarrerasEnCombo(combocarrera);
            tabla.TablaCarrera(tablacarreras);
        } else {
            JOptionPane.showMessageDialog(null, "Error al guardar la carrera.");
        }

    }//GEN-LAST:event_bcrearcarreraActionPerformed

    private void bmodificarcarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bmodificarcarreraActionPerformed
        // TODO add your handling code here:
        int fila = tablacarreras.getSelectedRow();

        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Selecciona una carrera para modificar.");
            return;
        }

        String idStr = tablacarreras.getValueAt(fila, 0).toString();
        int id = Integer.parseInt(idStr);

        String nuevoNombre = tcarrerac.getText().trim();

        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa el nuevo nombre de la carrera.");
            return;
        }

        Carrera nuevacarrrera = new Carrera(id, nuevoNombre);
        CarreraDAO dao = new CarreraDAO();

        if (dao.modificarCarrera(nuevacarrrera)) {
            JOptionPane.showMessageDialog(null, "Carrera modificada correctamente.");
            tcarrerac.setText("");
            tabla.TablaCarrera(tablacarreras);
            cargarCarrerasEnCombo(cbcarrea);
            cargarCarrerasEnCombo(combocarrera);
        } else {
            JOptionPane.showMessageDialog(null, "Error al modificar la carrera.");
        }
    }//GEN-LAST:event_bmodificarcarreraActionPerformed

