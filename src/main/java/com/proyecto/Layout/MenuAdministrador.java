    private void bguardaralumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bguardaralumnoActionPerformed
        // TODO add your handling code here:
        String nombre = tnombrealumno.getText().trim();
        String apellido = tapellidosalumno.getText().trim();
        String dni = tdnialumno.getText().trim();
        String correo = tcorreoalumno.getText().trim();
        String carreraAlumno = combocarrera.getSelectedItem().toString();

        if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty() || correo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos deben estar completos.");
            return;
        }

        Alumno crearalumno = new Alumno(0, nombre, apellido, dni, correo, carreraAlumno);
        AlumnoDAO dao = new AlumnoDAO();

        if (dao.guardarAlumno(crearalumno)) {
            JOptionPane.showMessageDialog(null, "Alumno guardado correctamente.");
            limpiarCamposAlumno();
            tabla.TablaAlumno(tablaalumnos); // Si tienes m√©todo para refrescar la tabla
        } else {
            JOptionPane.showMessageDialog(null, "Error al guardar alumno.");
        }
    }//GEN-LAST:event_bguardaralumnoActionPerformed

